def Seleccion(dicc):
    notas = list() # Lista para las notas no retiradas    
    nota_ganador = 0 # Variable de control para almacenar la nota mas alta del promedio de notas
    est_ganador = list() # Lista de salida del estudiante ganador
    creditos = 0 # Variable control para acumular los creditos
    for id in dicc: # Se recorre el diccionario        
        materias = dicc[id]["materias"] # Contiene las materias
        nombre = dicc[id]["nombres"] # Contiene nombre
        apellidos = dicc[id]["apellidos"] # Contiene Apellidos
        documento = dicc[id]["documento"] # Contiene documento
        programa = dicc[id]["programa"] # Contiene el programa
        correo = "" # Variable para retornar el correo
        for materia in materias: # Recorre las materias
            if materia["retirada"] == "No": # Condicion para controlar solo las variables no retiradas
                n = materia["nota"] * materia["creditos"] # Materias * Creditos
                creditos += materia["creditos"] # Suma los creditos de las materias no retiradas
                notas.append(n) # Agrego las notas a la lista          
        promedio = sum(notas)/creditos # Promedio
        if promedio > nota_ganador:  # Si promedio es mayor entonces agrego los valores para retornar              
            est_ganador.clear() # Siempre borra antes, por lo que siempre esta el promedio mas alto
            est_ganador.append(id)
            est_ganador.append(nombre)
            est_ganador.append(apellidos)
            est_ganador.append(documento)
            est_ganador.append(programa)
            est_ganador.append(promedio)
            est_ganador.append(correo)
            nota_ganador = promedio  # Igualo nota_ganador para que siempre esta igualada a la nota promedio alta          
    return est_ganador

